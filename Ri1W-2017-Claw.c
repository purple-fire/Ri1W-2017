#pragma config(Motor,  port2,           HMotor,        tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port3,           FrontR,        tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           FrontL,        tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           CLAW,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port6,           LARM,          tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port7,           RARM,          tmotorVex393_MC29, openLoop, reversed)
#pragma config(Motor,  port10,          BackL,         tmotorVex393_HBridge, openLoop)
